def split_brief_full(text: str) -> tuple[str, str]:
    """Split an LLM response into (brief, full)."""
    if SEPARATOR in text:
        brief, _, full = text.partition(SEPARATOR)
        return brief.strip(), full.strip()
    return text.strip(), text.strip()
