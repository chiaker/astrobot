def _persona_extra(user: User | None) -> str:
    if user is None:
        return ""
    parts: list[str] = []

    name = user.display_name
    gender = user.gender

    if name and gender == "m":
        parts.append(
            f"Пользователя зовут {name}, это мужчина. "
            "Обращайся к нему по имени, используй мужской род при согласовании."
        )
    elif name and gender == "f":
        parts.append(
            f"Пользователя зовут {name}, это женщина. "
            "Обращайся к ней по имени, используй женский род при согласовании."
        )
    elif name:
        parts.append(f"Пользователя зовут {name}. Обращайся по имени.")
    elif gender == "m":
        parts.append("Пользователь — мужчина. Используй мужской род при обращении.")
    elif gender == "f":
        parts.append("Пользователь — женщина. Используй женский род при обращении.")

    if not getattr(user, "astro_terms_enabled", True):
        parts.append(
            "РЕЖИМ «БЕЗ ТЕРМИНОВ» — строго и буквально.\n"
            "Пользователь не знаком с астрологией. "
            "Все астрологические позиции НУЖНО переводить в качества человека и жизненные темы.\n\n"
            "ЗАПРЕЩЕНО называть:\n"
            "• Планеты в знаках зодиака: «Луна в Овне», «Солнце в Рыбах», «Марс в Скорпионе»\n"
            "• Названия знаков зодиака как технических единиц: «Овен», «Телец», «Лев», «Скорпион»...\n"
            "• Планеты в домах: «Меркурий в 7-м доме», «Венера на Асценденте»\n"
            "• Номера домов: «1-й дом», «8-й дом», «куспид 4-го»\n"
            "• Аспекты: квадратура, оппозиция, трин, секстиль, соединение, квинкунс\n"
            "• Технические термины: транзит, прогрессия, ретроград, синастрия, диспозитор\n\n"
            "КАК ПЕРЕВОДИТЬ — примеры (переводи В ТАКОМ ДУХЕ, не копируй дословно):\n"
            "❌ «Луна в Овне» → ✅ «твои эмоции яркие и порывистые, ты быстро загораешься»\n"
            "❌ «Солнце в Рыбах» → ✅ «в твоей личности много интуиции, чуткости и мечтательности»\n"
            "❌ «Марс в 7-м доме» → ✅ «в отношениях ты активен и иногда нетерпелив»\n"
            "❌ «Сатурн квадратурит Венеру» → ✅ «в любви есть внутреннее напряжение между желанием близости и страхом ограничений»\n"
            "❌ «Меркурий ретроградный» → ✅ «твоё мышление любит возвращаться и переосмыслять уже пройденное»\n"
            "❌ «Транзитный Юпитер входит в 10-й дом» → ✅ «сейчас хорошее время для роста в карьере и профессиональных успехов»\n\n"
            "ГОВОРИ о человеке и его жизни — не о небесных позициях."
        )

    if not parts:
        return ""
    return "\n".join(parts) + "\n\n"


def build_system_natal(user: User | None = None) -> str:
    return ASTRA_PERSONA + _persona_extra(user) + _NATAL_TASK


def build_system_horoscope(user: User | None = None) -> str:
    return ASTRA_PERSONA + _persona_extra(user) + _HOROSCOPE_TASK


def build_system_question(user: User | None = None) -> str:
    return ASTRA_PERSONA + _persona_extra(user) + _QUESTION_TASK


# Backward-compatible aliases (no user context)
SYSTEM_NATAL = build_system_natal()
SYSTEM_HOROSCOPE = build_system_horoscope()
SYSTEM_QUESTION = build_system_question()


def split_brief_full(text: str) -> tuple[str, str]:
    if SEPARATOR in text:
        brief, _, full = text.partition(SEPARATOR)
        return brief.strip(), full.strip()
    return text.strip(), text.strip()
